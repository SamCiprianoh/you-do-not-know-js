// Atribuição: = 
a = 2

//Aritmético: + (adição), - (subtração), * (multiplicação), e / (divisão)
a * 2

//Atribuição com Operação: +=, -=, *=, e /= são operadores comuns que combinam operadores aritméticos com a atribuição
a += 2 //(o mesmo que a = a + 2)

//Incremento/Decremento: ++ (incremento), -- (decremento)
 a++ //(similar à a = a + 1)

//Igualdade: == (igualdade), === (igualdade estrita), != (desigualdade), !== (desigualdade estrita)
 a == b 

//Comparação: < (menor que), > (maior que), <= (menor ou igual), >= (maior ou igual)
 a <= b

//Lógicos: && (e), || (ou)
 a || b
 