//=== Código ===//
// Código é um conjunto de instruções especializadas para dizer ao computador quais tarefas ele deve realizar

// As regras para formatos e combinações de instruções é chamado 'linguagem de computação', algumas vezes referenciado como sintaxe

//---------------------------------------------------------------------------------------------------------------------------------------//




//=== Instruções ===//

// Em liguagem de computação, um grupo de palavras, números e operadores que realizam tarefas específicas são uma instrução

// Exemplo
a = b * 2 // Uma instrução em JavaScript

// Os caracteres 'a' e 'b' são chamados variáveis, são recipientes para amarzenar qualquer coisa dentro. (variáveis detém valores)

// Em contrapartida, o '2' é apenas um valor, chamado valor literal, por que é apresentado sozinho, sem estar armazenado em uma variável.

// Os caracteres '=' e '*' são operadores, eles realizam ações com os valores e variáveis 
//---------------------------------------------------------------------------------------------------------------------------------------//





//=== Expressões ===//
// As instruções são feitas de uma ou mais expressões
// Uma expressão é qualquer referência à uma variável ou valor, ou um conjunto de variáveis e valores combinados com operadores

// Exemplo:
a = b * 2 // Essa instrução tem quantro expressões dentro dela

// 1ª) 2 é uma expressão de valor literal
// 2ª) b é uma expressão de valor variável
// 3ª) a * 2 é uma expressão aritmética
// 4ª) a = b * 2 é uma expressão de atribuição, significa designar o resultado da expressão b * 2 para a variável a]


// Uma instrução de expressão mais comum é chamada 'instrução de expressão de chamada', sendo a própria chamada da função uma instrução completa
alert(a)